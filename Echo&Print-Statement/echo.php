	echo "Dhaka" . " University\n"; //String Concatenation in PHP
	echo "<b>Dhaka" . " University</b>\n";
	echo "<h1><i>Dhaka" . " University</i><h1>\n";
	echo 56.40;
	echo "\n";
?>

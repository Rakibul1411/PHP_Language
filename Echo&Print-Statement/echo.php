<?php
	echo "Dhaka University\n";
	echo 'Dhaka University';
	echo "\n";
	echo('Dhaka University');
	echo "\n";
	/*Difference between , and . : The comma uses multiple arguments in echo,
	while the dot concatenates the strings before passing them to echo.*/
